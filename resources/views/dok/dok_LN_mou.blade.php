    </div>
</div>
<div class="row mt-3">
    <div class="col-lg-1">
        3.5
    </div>
    <div class="col-lg-11">
        During the suspension period, the SECOND PARTY and the THIRD PARTY shall not give misleading certification status and shall not use the SNI marking on the product since the suspension date.
    </div>
</div>
<div class="row mt-3">
    <div class="col-lg-1">
        3.6
    </div>
    <div class="col-lg-11">
        Whenever there is no corrective action during the suspension period, the FIRST PARTY will withdraw the SNI certificate.
    </div>
</div>
<div class="row mt-3">
    <div class="col-lg-1">
        3.7
    </div>
    <div class="col-lg-11">
        The THIRD PARTY will discontinue use of SNI certificate upon suspension or withdrawal of its certificate by the FIRST PARTY
    </div>
</div>

