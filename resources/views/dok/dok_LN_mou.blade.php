    </div>
</div>

<p class="text-center mt-3">
    <b>ARTICLE 3 <br>
    The Certificate Validity</b>
</p>

<div class="row mt-3">
    <div class="col-lg-1">
        3.1
    </div>
    <div class="col-lg-11">
        Product certification will be valid for 4 (four) years unless the product could not fulfill the SNI standard upon evaluation by the FIRST PARTY or subjected to cancellation, suspension and withdrawal with certain cause. Before end of its validity, the FIRST PARTY will conduct the recertification at the SECOND PARTY location.
    </div>
</div>
<div class="row mt-3">
    <div class="col-lg-1">
        3.2
    </div>
    <div class="col-lg-11">
        During the certification validity, the FIRST PARTY will conduct the surveillance to the SECOND PARTY within less than 12 (twelve months) since the SECOND PARTY been certified and within 12 (twelve) months maximum for the next visit.
    </div>
</div>
<div class="row mt-3">
    <div class="col-lg-1">
        3.3
    </div>
    <div class="col-lg-11">
        The SECOND PARTY shall follow up the non-conformity arise during surveillance within prescribed period.
    </div>
</div>
<div class="row mt-3">
    <div class="col-lg-1">
        3.4
    </div>
    <div class="col-lg-11">
