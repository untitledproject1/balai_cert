<p class="mt-2 ml-5">The parties hereto have agreed to enter into this Memorandum of Agreement in accordance with the implementation of Conformity Assessment for Laminated Safety Glass for Motor Vehicles, under the following terms and conditions:</p>

<p class="text-center mt-3">
    <b>ARTICLE 1</b> <br>
    Scope of Services
</p>

<div class="row mt-3">
    <div class="col-lg-1">
        1.1
    </div>
    <div class="col-lg-11">
        As requested by THIRD PARTY to the FIRST PARTY to obtain the Product Sertification on using SNI marking according to SNI 15-0048-2005/Amd 1:2014 laminated safety glass for motor vehicles standard, the FIRST PARTY agreed to give services on product certification to the THIRD PARTY based on Certification system type 5 as described in this contract.
    </div>
</div>
<div class="row mt-3">
    <div class="col-lg-1">
        1.2
    </div>
    <div class="col-lg-11">
        The FIRST PARTY will assign competence auditor and sampling officer to conduct audit processes and conformity assessment to the SECOND PARTY as well as product sampling based on Certification Body procedure and SNI requirement.
    </div>
</div>
<div class="row mt-3">
    <div class="col-lg-1">
        1.3
    </div>
    <div class="col-lg-11">
        During the product certification service The First Party will provide competence auditor from internal sources and/or external souces if necessary as the FIRST PARTY’s subcontractor. The FIRST PARTY shall mantain the confidentially information relating to the SECOND AND THIRD PARTY
    </div>
</div>
<div class="row mt-3">
    <div class="col-lg-1">
        1.4
    </div>
    <div class="col-lg-11">
        The Auditor assigned by the FIRST PARTY will conduct the confirmity assessment and the sampling officer will take some samples to the SECOND PARTY based on Certification Body procedures and SNI standard.
    </div>
</div>
<div class="row mt-3">
    <div class="col-lg-1">
        1.5
    </div>
    <div class="col-lg-11">
        The Certification process cover the Audit Stage-1, Audit Stage-2, Certification, Surveillance, Recertification and sampling process. Audit Stage-1 will start whenever document necessary received by the FIRST PARTY and fulfill the administration requirement.
    </div>
</div>
<div class="row mt-3">
    <div class="col-lg-1">
        1.6
    </div>
    <div class="col-lg-11">
        Conformity Assessment and sampling process will be done at the SECOND PARTY location. The SECOND PARTY shall provide necessary access to audit team and sampling officer as well as personnel to accompany the audit team when necessary.
    </div>
</div>

<p class="text-center mt-3">
    <b>ARTICLE 2 <br>
    Certification</b>
</p>

<div class="row mt-3">
    <div class="col-lg-1">
        2.1
    </div>
    <div class="col-lg-11">
